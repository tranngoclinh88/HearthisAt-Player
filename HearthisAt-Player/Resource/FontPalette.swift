import UIKit

extension UIFont {
    
    static var hta_title: UIFont {
        return UIFont.systemFont(ofSize: 20.0, weight: UIFontWeightMedium)
    }
    
    static var hta_subtitle: UIFont {
        return UIFont.systemFont(ofSize: 17.0, weight: UIFontWeightRegular)
    }
}
